// binary operators
// and & or | xor ^  not ~  left shift <<  right shift >>
let a=10;
let b=5;
console.log(a&b)    //0
console.log(b&a)    //0
console.log(a|b)    //15
console.log(b|a)    //15 
console.log(a^b)    //15
console.log(~a)     //-11
console.log(~b)     //-6
console.log(a<<b)   //320
console.log(a>>b)   //0
console.log(b<<a)   //5120
console.log(b>>a)   //0